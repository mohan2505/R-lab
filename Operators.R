# Arithmetic Operators
a <- 10
b <- 3

cat("Arithmetic Operators:\n")
cat("a + b =", a + b, "\n")   # Addition
cat("a - b =", a - b, "\n")   # Subtraction
cat("a * b =", a * b, "\n")   # Multiplication
cat("a / b =", a / b, "\n")   # Division
cat("a ^ b =", a ^ b, "\n")   # Exponent
cat("a %% b =", a %% b, "\n") # Modulus
cat("a %/% b =", a %/% b, "\n\n") # Integer division

# Relational Operators
cat("Relational Operators:\n")
cat("a == b:", a == b, "\n")
cat("a != b:", a != b, "\n")
cat("a > b :", a > b, "\n")
cat("a < b :", a < b, "\n")
cat("a >= b:", a >= b, "\n")
cat("a <= b:", a <= b, "\n\n")

# Logical Operators
x <- TRUE
y <- FALSE

cat("Logical Operators:\n")
cat("x & y  :", x & y, "\n")   # AND
cat("x | y  :", x | y, "\n")   # OR
cat("!x     :", !x, "\n\n")    # NOT

# Assignment Operators
cat("Assignment Operators:\n")
z = 5        # '=' operator
w <- 7       # '<-' operator
k -> m       # value of k assigned to m
k <- 6
cat("z =", z, "\n")
cat("w =", w, "\n")
cat("m =", m, "\n\n")

# Miscellaneous Operators
cat("Miscellaneous Operators:\n")
v <- c(10, 20, 30, 40)
cat("v[2] =", v[2], "\n")  # Indexing operator

cat("Sequence 1:5 =", 1:5, "\n")#sequence operator
